    private void popuniFormu(Tiket tiket) {
        try {
            radnikField.setText(tiket.getRadnik().toString());
            korisnikField.setText(tiket.getKorisnik().toString());
            statusComboBox.removeAllItems();
            statusComboBox.addItem("Na čekanju"); 
            statusComboBox.addItem("Dobitan");    
            statusComboBox.addItem("Gubitan");    
            statusComboBox.setSelectedIndex(tiket.getStatus());

            ulogField.setText(String.valueOf(tiket.getUlog()));
            kvotaField.setText(String.valueOf(tiket.getUkupnaKvota()));
            dobitakField.setText(String.valueOf(tiket.getMoguciDobitak()));

            ParTableModel model = new ParTableModel(tiket.getParovi());
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Greška prilikom učitavanja tiketa: " + e.getMessage());
        }
    }
