    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Tiket noviTiket = new Tiket();
            noviTiket.setIdTiket(originalniTiket.getIdTiket());
            noviTiket.setStatus(statusComboBox.getSelectedIndex());
            
            ServerskiOdgovor so = KlijentController.getInstance().izmeniTiket(noviTiket);
            if (so.isUspesno()) {
                JOptionPane.showMessageDialog(this, "Status tiketa je uspešno promenjen!");
                forma.popuniTikete(new Tiket());
            } else {
                JOptionPane.showMessageDialog(this, "Greška: " + so.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greška prilikom menjanja statusa: " + ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

